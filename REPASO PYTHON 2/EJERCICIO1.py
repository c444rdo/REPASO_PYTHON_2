# Definir una función max() que tome dos números y devuelva el mayor de ellos.
def max(num1, num2):
    # Compara los dos números y devuelve el mayor.
    if num1 > num2:
        return num1
    else:
        return num2

# Ejemplo de uso
print(max(3, 7))  # Salida: 7